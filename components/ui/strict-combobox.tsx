"use client";

import * as React from "react";
import { Check, ChevronsUpDown } from "lucide-react";

import { cn } from "@/lib/utils";
import { Button } from "@/components/ui/button";
import { Popover, PopoverContent, PopoverTrigger } from "@/components/ui/popover";
import {
  Command,
  CommandEmpty,
  CommandGroup,
  CommandInput,
  CommandItem,
} from "@/components/ui/command";

export type StrictComboboxOption = {
  id: string;
  label: string;
  keywords?: string;
};

type Props = {
  options: StrictComboboxOption[];
  valueId: string;
  onChange: (nextId: string) => void;

  placeholder?: string;
  searchPlaceholder?: string;
  emptyText?: string;

  disabled?: boolean;

  allowClear?: boolean;
  clearLabel?: string;
};

export function StrictCombobox({
  options,
  valueId,
  onChange,
  placeholder = "Select…",
  searchPlaceholder = "Search…",
  emptyText = "No results.",
  disabled = false,
  allowClear = false,
  clearLabel = "Clear",
}: Props) {
  const [open, setOpen] = React.useState(false);
  const [typed, setTyped] = React.useState("");

  const selected = React.useMemo(() => {
    const v = String(valueId ?? "");
    return options.find((o) => String(o.id) === v) || null;
  }, [options, valueId]);

  const filtered = React.useMemo(() => {
    const q = typed.trim().toLowerCase();
    if (!q) return options;

    return options.filter((o) => {
      const label = String(o.label || "").toLowerCase();
      const kw = String(o.keywords || "").toLowerCase();
      return label.includes(q) || kw.includes(q);
    });
  }, [options, typed]);

  // strict behavior: do not commit typed text
  React.useEffect(() => {
    if (!open) setTyped("");
  }, [open]);

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild disabled={disabled}>
        <Button
          variant="outline"
          role="combobox"
          aria-expanded={open}
          className={cn("w-full justify-between", disabled && "opacity-60")}
        >
          <span className="truncate">
            {selected ? selected.label : placeholder}
          </span>
          <ChevronsUpDown className="ml-2 h-4 w-4 shrink-0 opacity-50" />
        </Button>
      </PopoverTrigger>

      <PopoverContent className="w-[--radix-popover-trigger-width] p-0" align="start">
        <Command>
          <CommandInput
            placeholder={searchPlaceholder}
            value={typed}
            onValueChange={setTyped}
          />

          <CommandEmpty>{emptyText}</CommandEmpty>

          <CommandGroup>
            {allowClear ? (
              <CommandItem
                value="__clear__"
                onSelect={() => {
                  onChange("");
                  setOpen(false);
                }}
              >
                <Check
                  className={cn(
                    "mr-2 h-4 w-4",
                    String(valueId) === "" ? "opacity-100" : "opacity-0"
                  )}
                />
                {clearLabel}
              </CommandItem>
            ) : null}

            {filtered.map((opt) => {
              const optId = String(opt.id);
              const isSelected = optId === String(valueId);

              return (
                <CommandItem
                  key={optId}
                  value={optId}
                  onSelect={() => {
                    onChange(optId); // ✅ always string
                    setOpen(false);
                  }}
                >
                  <Check
                    className={cn(
                      "mr-2 h-4 w-4",
                      isSelected ? "opacity-100" : "opacity-0"
                    )}
                  />
                  <span className="truncate">{opt.label}</span>
                </CommandItem>
              );
            })}
          </CommandGroup>
        </Command>
      </PopoverContent>
    </Popover>
  );
}
